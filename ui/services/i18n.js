let currentLang = (() => {
  if (typeof window === 'undefined') return 'en';
  return window.localStorage.getItem('fp2_lang') || 'en';
})();

function interpolate(template, vars = {}) {
  return String(template).replace(/\{(\w+)\}/g, (_match, key) => {
    const value = vars[key];
    return value === undefined || value === null ? '' : String(value);
  });
}

function resolveTranslation(lang, key) {
  return translations[lang]?.[key] ?? translations.en?.[key] ?? key;
}

function getPluralForm(lang, count) {
  if (lang === 'ru') {
    const mod10 = Math.abs(count) % 10;
    const mod100 = Math.abs(count) % 100;
    if (mod10 === 1 && mod100 !== 11) return 'one';
    if (mod10 >= 2 && mod10 <= 4 && (mod100 < 12 || mod100 > 14)) return 'few';
    return 'many';
  }
  return Math.abs(count) === 1 ? 'one' : 'many';
}

export function setLanguage(lang) {
  currentLang = lang === 'ru' ? 'ru' : 'en';
  if (typeof window !== 'undefined') {
    window.localStorage.setItem('fp2_lang', currentLang);
    document.documentElement.lang = currentLang;
  }
}

export function getCurrentLang() {
  return currentLang;
}

export function t(key, vars = {}) {
  const entry = resolveTranslation(currentLang, key);
  if (typeof entry === 'object' && entry !== null) {
    return interpolate(entry.many ?? entry.one ?? key, vars);
  }
  return interpolate(entry, vars);
}

export function tp(key, count, vars = {}) {
  const entry = resolveTranslation(currentLang, key);
  if (typeof entry === 'object' && entry !== null) {
    const form = getPluralForm(currentLang, Number(count) || 0);
    return interpolate(entry[form] ?? entry.many ?? entry.one ?? key, { ...vars, count });
  }
  return interpolate(entry, { ...vars, count });
}

export function translateDocument(root = document) {
  root.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.getAttribute('data-i18n');
    if (key) {
      element.textContent = t(key);
    }
  });

  root.querySelectorAll('[data-i18n-title]').forEach((element) => {
    const key = element.getAttribute('data-i18n-title');
    if (key) {
      element.setAttribute('title', t(key));
    }
  });
}

if (typeof window !== 'undefined') {
  window.i18n = translations;
  window.t = t;
  window.getCurrentLang = getCurrentLang;
}

export { translations as i18n };
