function normalizeLocale(locale) {
  return String(locale || 'ru').toLowerCase().startsWith('en') ? 'en' : 'ru';
}

export function getOperatorCopy(locale = 'ru') {
  return UI_COPY[normalizeLocale(locale)] || UI_COPY.ru;
}

export function localizeOperatorToken(value, locale = 'ru') {
  const copy = getOperatorCopy(locale);
  if (value == null || value === '') {
    return copy.common.unknown;
  }
  const raw = String(value);
  return copy.tokens[raw] || raw.replaceAll('_', ' ');
}
